package bit_manipulation;

public class binary_AND {
    public static void main(String[] args) {
        System.out.println(5 & 6); // 4

        // 5 --> 0101
        // 6 --> 0110
        // 5 & 6 --> 4

        //// 101
        // & 110
        // --------
        //// 100
        // ---------

        // 100 -> 4 (decimal)
    }

}
